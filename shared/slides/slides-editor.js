    /** Get nested value: getDeep(obj, 'left.title') */
    static getDeep(obj, path) {
        return path.split('.').reduce((o, k) => o?.[k], obj);
    }

    /** Set nested value: setDeep(obj, 'left.title', 'foo') */
    static setDeep(obj, path, value) {
        const keys = path.split('.');
        const last = keys.pop();
        const target = keys.reduce((o, k) => {
            if (o[k] == null) o[k] = {};
            return o[k];
        }, obj);
        target[last] = value;
    }
