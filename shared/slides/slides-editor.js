/**
 * Repair JSON text generated by AI (Gemini, ChatGPT…) that may contain:
 * - Literal newlines inside string values (stripped)
 * - Unescaped double-quotes inside string values (escaped to \")
 * Uses structure-aware parsing to distinguish real string boundaries
 * from unescaped quotes (tracks object vs array context).
 */
function _repairJsonText(raw) {
    const n = raw.length;
    const out = [];
    let i = 0;

    function skipWs() { while (i < n && ' \t\n\r'.includes(raw[i])) i++; }

    function readString(ctx) {
        // ctx: 'key' | 'obj-val' | 'arr-val'
        // We are at the opening "
        out.push('"'); i++;
        while (i < n) {
            const ch = raw[i];
            if (ch === '\\') { out.push(ch); i++; if (i < n) { out.push(raw[i]); i++; } continue; }
            if (ch === '\n' || ch === '\r') { i++; continue; } // strip literal newlines
            if (ch === '"') {
                // Look ahead past whitespace to decide if this closes the string
                let j = i + 1;
                while (j < n && ' \t\n\r'.includes(raw[j])) j++;
                const nxt = j < n ? raw[j] : '';
                let isClose = false;
                if (ctx === 'key') {
                    isClose = (nxt === ':');
                } else if (ctx === 'obj-val') {
                    if (nxt === '}' || nxt === ']' || nxt === '') { isClose = true; }
                    else if (nxt === ',') {
                        // In object, after comma we expect "key": — validate lookahead
                        let k = j + 1;
                        while (k < n && ' \t\n\r'.includes(raw[k])) k++;
                        if (k < n && raw[k] === '"') {
                            // Find end of potential key
                            k++;
                            while (k < n) { if (raw[k] === '\\') { k += 2; continue; } if (raw[k] === '"') break; k++; }
                            if (k < n) {
                                k++; // past "
                                while (k < n && ' \t\n\r'.includes(raw[k])) k++;
                                isClose = (k < n && raw[k] === ':');
                            }
                        } else if (k < n && raw[k] === '}') {
                            // trailing comma before }
                            isClose = true;
                        }
                    }
                } else { // arr-val
                    isClose = (nxt === ']' || nxt === ',' || nxt === '}' || nxt === '');
                }
                if (isClose) { out.push('"'); i++; return; }
                // Not a real close — escape it
                out.push('\\"'); i++; continue;
            }
            out.push(ch); i++;
        }
    }

    function readValue(ctx) {
        skipWs(); if (i >= n) return;
        const ch = raw[i];
        if (ch === '"') readString(ctx);
        else if (ch === '{') readObject();
        else if (ch === '[') readArray();
        else if ('tfn'.includes(ch)) { while (i < n && raw[i] >= 'a' && raw[i] <= 'z') { out.push(raw[i]); i++; } }
        else if (ch === '-' || (ch >= '0' && ch <= '9')) { while (i < n && '-+0123456789.eE'.includes(raw[i])) { out.push(raw[i]); i++; } }
    }

    function readObject() {
        out.push('{'); i++; skipWs();
        if (i < n && raw[i] === '}') { out.push('}'); i++; return; }
        while (i < n) {
            skipWs();
            if (i < n && raw[i] === '"') readString('key');
            skipWs();
            if (i < n && raw[i] === ':') { out.push(':'); i++; }
            skipWs();
            readValue('obj-val');
            skipWs();
            if (i < n && raw[i] === ',') { out.push(','); i++; }
            else if (i < n && raw[i] === '}') { out.push('}'); i++; return; }
            else break;
        }
    }

    function readArray() {
        out.push('['); i++; skipWs();
        if (i < n && raw[i] === ']') { out.push(']'); i++; return; }
        while (i < n) {
            skipWs();
            readValue('arr-val');
            skipWs();
            if (i < n && raw[i] === ',') { out.push(','); i++; }
            else if (i < n && raw[i] === ']') { out.push(']'); i++; return; }
            else break;
        }
    }

    skipWs();
    if (i < n && raw[i] === '{') readObject();
    else if (i < n && raw[i] === '[') readArray();
    else return raw; // not JSON object/array, return as-is

    return out.join('');
}
window._repairJsonText = _repairJsonText;

